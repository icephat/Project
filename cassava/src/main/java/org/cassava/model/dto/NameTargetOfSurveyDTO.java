package org.cassava.model.dto;


public class NameTargetOfSurveyDTO {
	
	private String name ;
	private String num ;
	private String percent ;
	private String avgDamageLevel ;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getPercent() {
		return percent;
	}
	public void setPercent(String percent) {
		this.percent = percent;
	}
	public String getAvgDamageLevel() {
		return avgDamageLevel;
	}
	public void setAvgDamageLevel(String avgDamageLevel) {
		this.avgDamageLevel = avgDamageLevel;
	}
	
	
}
